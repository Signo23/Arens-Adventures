package it.aren.common;

/**
 * Games's states.
 */
public enum ApplicationState {
    /**
     * BOOT state of application.
     */
    BOOT,
    /**
     * MENU state of application.
     */
    MENU,
    /**
     * MENU_SETTINGS state of application.
     */
    MENU_SETTINGS,
    /**
     * GAME state of application.
     */
    GAME,
    /**
     * GAME_DIALOG state of application.
     */
    GAME_DIALOG,
    CLOSE;
}
